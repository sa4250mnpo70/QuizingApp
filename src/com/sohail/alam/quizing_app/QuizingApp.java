    private void login() {
        String username = jTextField_username.getText();
        String password = jPasswordField_password.getText();
        if (!isLoggedIn) {
            //login for normal users
            isLoggedIn = methods.login(username, password);
            //if login is successful
            if (isLoggedIn) {
                jToggleButton_login.setBackground(Color.RED);
                jToggleButton_login.setText("Logout");
                jTextField_username.setEnabled(false);
                jPasswordField_password.setEnabled(false);
                jTabbedPane_manageQuiz.setVisible(false);
                jLabel_userAccessStatus.setForeground(Color.BLACK);
                jLabel_userAccessStatus.setText("Successfully Logged In");
            } else {
                //try Admin login
                isLoggedIn = methods.loginAdmin(username, password);
                if (isLoggedIn) {
                    jToggleButton_login.setBackground(Color.RED);
                    jToggleButton_login.setText("Logout");
                    jTextField_username.setEnabled(false);
                    jPasswordField_password.setEnabled(false);
                    jTabbedPane_manageQuiz.setVisible(true);
                    jLabel_userAccessStatus.setForeground(Color.BLUE);
                    jLabel_userAccessStatus.setText("Admin login Successful");
                } else {
                    jLabel_userAccessStatus.setForeground(Color.RED);
                    jLabel_userAccessStatus.setText("Login Unsuccessful. Check user credentials");
                    jToggleButton_login.setSelected(false);
                }
            }
        } else {
            //logout
            isLoggedIn = !(methods.logout(username));
            //if logout is successful
            if (!isLoggedIn) {
                jToggleButton_login.setBackground(Color.GRAY);
                jToggleButton_login.setText("Login");
                jTextField_username.setEnabled(true);
                jPasswordField_password.setEnabled(true);
                jTabbedPane_manageQuiz.setVisible(false);
                jLabel_userAccessStatus.setText("");
            }
        }
    }
