    private void jTextArea_createQuizQuestionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextArea_createQuizQuestionKeyTyped
        int count = 0;
        if (!jTextArea_createQuizQuestion.getText().trim().isEmpty()) {
            jButton_createQuizSave.setEnabled(true);
            jButton_selectImage.setEnabled(true);
            count = Integer.parseInt(jTextField_totalQuestions.getText());
            if (currentQuestionNumber > 0) {
                jButton_createQuizPrevious.setEnabled(true);
            }
            if (count > currentQuestionNumber) {
                jButton_createQuizNext.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jTextArea_createQuizQuestionKeyTyped

    private void jButton_createQuizSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_createQuizSaveActionPerformed
        String question = jTextArea_createQuizQuestion.getText().trim();
        int thisQuestion = Integer.parseInt(jTextField_totalQuestions.getText());
        int difficulty = Integer.parseInt(jComboBox_createQuizDifficultyLevel.getSelectedItem().toString());
        String image = null;
        if (currentQuestionNumber == 0) {
            currentQuestionNumber = thisQuestion + 1;
        } else {
            currentQuestionNumber++;
        }
        if (!question.isEmpty()) {
            if (image == null) {
//                questions = new Questions[currentQuestionNumber];
                questions[currentQuestionNumber - 1] = new Questions(currentQuestionNumber, difficulty, question);
            } else {
//                questions = new Questions[questionNumber];
                questions[currentQuestionNumber - 1] = new Questions(currentQuestionNumber, difficulty, question, image);
            }
            jTextField_totalQuestions.setText(String.valueOf(currentQuestionNumber));
        }
        jTextArea_createQuizQuestion.setText("");
        jTextArea_createQuizQuestion.requestFocus();
//        jButton_createQuizSave.setEnabled(false);
//        jButton_createQuizAddOption.setEnabled(true);
//        jButton_createQuizAddOption.requestFocus();
        //TEST
        System.out.println("");
        System.out.println("Question Number: " + questions[currentQuestionNumber - 1].getQuestionNo());
        System.out.println("Question Difficulty: " + questions[currentQuestionNumber - 1].getDifficulty());
        System.out.println("Question Text: " + questions[currentQuestionNumber - 1].getText());
        System.out.println("Question Image: " + questions[currentQuestionNumber - 1].getImage());
        System.out.println("Question UUID: " + questions[currentQuestionNumber - 1].getUuid());
        System.out.println("");
    }//GEN-LAST:event_jButton_createQuizSaveActionPerformed

